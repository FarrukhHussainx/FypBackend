const mongoose = require("mongoose");
const { Schema } = mongoose;
const customersSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  address: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});
const customers = mongoose.model("customers", customersSchema);

module.exports = customers;
